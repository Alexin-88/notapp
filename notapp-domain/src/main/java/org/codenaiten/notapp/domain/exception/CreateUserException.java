package org.codenaiten.notapp.domain.exception;

import org.codenaiten.notapp.domain.entity.User;

public class CreateUserException extends Exception{

    private final User user;



// ------------------------------------------------------------------------------------------------------------------ \\
// ---| CONSTRUCTOR |------------------------------------------------------------------------------------------------ \\
// ------------------------------------------------------------------------------------------------------------------ \\

    public CreateUserException(final User user, final String message) {
        super(message);
        this.user = user;
    }



// ------------------------------------------------------------------------------------------------------------------ \\
// ---| GETTERS |---------------------------------------------------------------------------------------------------- \\
// ------------------------------------------------------------------------------------------------------------------ \\

    public User getUser() {
        return this.user;
    }

// ------------------------------------------------------------------------------------------------------------------ \\

}
